package main

import "fmt"

func main() {

	var name1 = "Husada"
	var name2 = "Budi"

	var result bool = name1 == name2
	fmt.Println(result)

	//comparasion value pada int
	var angka1 = 10
	var angka2 = 20

	fmt.Println(angka1 > angka2)
	fmt.Println(angka1 < angka2)
	fmt.Println(angka1 == angka2)
	fmt.Println(angka1 != angka2)

}
